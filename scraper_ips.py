# ── Parsers ───────────────────────────────────────────────────────────────────

def parse_generic_investigation_page(soup: BeautifulSoup, source_url: str, country: str) -> list[dict]:
    """Generic parser for investigation-style pages with article/post structure."""
    cases = []
    articles = soup.find_all(["article", "div"], class_=re.compile(r"post|case|entry|story", re.I))

    for article in articles[:20]:  # cap per page
        title_el = article.find(["h1", "h2", "h3", "h4"])
        if not title_el:
            continue
        title = title_el.get_text(strip=True)
        if len(title) < 5:
            continue

        content_el = article.find(["p", "div"], class_=re.compile(r"content|excerpt|body|text", re.I))
        if not content_el:
            all_p = article.find_all("p")
            content = " ".join(p.get_text(strip=True) for p in all_p[:3])
        else:
            content = content_el.get_text(strip=True)

        if len(content) < 20:
            continue

        from scraper_wiki import classify_type, assign_tier
        c_type = classify_type(content)
        tier, rarity = assign_tier(content, c_type)

        cases.append({
            "name":        title[:200],
            "location":    country,
            "country":     country,
            "type":        c_type,
            "description": content[:1000],
            "source":      source_url,
            "tier":        tier,
            "rarity":      rarity,
            "ips_case":    False,
            "scraped_at":  datetime.utcnow().isoformat()
        })

    return cases


def parse_ygs_page(soup: BeautifulSoup, source_url: str, country: str) -> list[dict]:
    """Parser for YourGhostStories.com listing pages."""
    cases = []
    story_links = soup.find_all("div", class_=re.compile(r"story|entry|post", re.I))

    for story in story_links[:15]:
        title_el = story.find(["h2", "h3", "a"])
        if not title_el:
            continue
        title = title_el.get_text(strip=True)
        if len(title) < 5:
            continue

        excerpt = story.get_text(strip=True)
        if len(excerpt) < 30:
            continue

        from scraper_wiki import classify_type, assign_tier
        c_type = classify_type(excerpt)
        tier, rarity = assign_tier(excerpt, c_type)

        cases.append({
            "name":        title[:200],
            "location":    country,
            "country":     country,
            "type":        c_type,
            "description": excerpt[:800],
            "source":      source_url,
            "tier":        tier,
            "rarity":      rarity,
            "ips_case":    False,
            "scraped_at":  datetime.utcnow().isoformat()
        })

    return cases


PARSERS = {
    "generic_investigation_page": parse_generic_investigation_page,
    "ygs_page":                   parse_ygs_page,
}


# ── Runner ────────────────────────────────────────────────────────────────────

def scrape_source(source: dict) -> list[dict]:
    url = source["url"]
    print(f"[SCRAPER IPS] Fetching: {url}")
    try:
        resp = requests.get(url, headers=HEADERS, timeout=15)
        resp.raise_for_status()
    except requests.RequestException as e:
        print(f"[ERROR] {url}: {e}")
        return []

    soup = BeautifulSoup(resp.text, "html.parser")
    parser_fn = PARSERS.get(source["parser"])
    if not parser_fn:
        print(f"[ERROR] No parser for {source['parser']}")
        return []

    return parser_fn(soup, url, source["country"])


def run():
    all_cases = []

    # Always start with hard-coded IPS known cases (authoritative)
    print(f"[SCRAPER IPS] Loading {len(IPS_KNOWN_CASES)} known IPS cases...")
    for c in IPS_KNOWN_CASES:
        c.setdefault("scraped_at", datetime.utcnow().isoformat())
        all_cases.append(c)

    # Then try live scraping
    for source in SOURCES:
        cases = scrape_source(source)
        all_cases.extend(cases)
        time.sleep(2)

    os.makedirs(os.path.dirname(OUTPUT_PATH), exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(all_cases, f, ensure_ascii=False, indent=2)

    print(f"[SCRAPER IPS] Done. {len(all_cases)} cases → {OUTPUT_PATH}")
    return all_cases


if __name__ == "__main__":
    run()
